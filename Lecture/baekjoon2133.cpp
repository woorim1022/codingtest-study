 //2 X n 타일링 문제 2 
 //짝수개일 때 마다 특별한한 경우가 두가지씩 더 존재하므로 점화식을 만들때 고려해야함 
  
#include <stdio.h>

int d[1001];

int dp(int x) {
    if(x == 0) return 1;
    if(x == 1) return 0;
    if(x == 2) return 3;
    if(d[x] != 0) return d[x];
    int result = 3 * dp(x - 2);
    for(int i = 3; i <= x; i++){
    	if(i % 2 == 0){  //짝수일 때 
    		result += 2 * dp(x -i);
		}
	}
    return d[x] = result;
}

int main(void) {
    int number;
    scanf("%d", &number);
    
    printf("%d", dp(number));
}
